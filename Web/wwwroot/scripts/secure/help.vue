			<div class="bg-white border rounded p-3" style="height: 500px; width: 300px;">
				<div class="overflow-auto" style="height: 430px;">
					<div v-for="chat in myChats" :key="chat.text" class="alert" :class="chat.isFromMe ? 'alert-light' : 'alert-info'">{{ chat.text }}</div>
				</div>
				<div class="d-flex">
					<input type="text" class="form-control" placeholder="Skriv spørsmål her" v-model="message" @keyup.enter="send()">
					<button type="button" class="btn btn-primary ms-2" @click="send()">
						<i class="fa-solid fa-paper-plane-top"></i>
					</button>
				</div>
