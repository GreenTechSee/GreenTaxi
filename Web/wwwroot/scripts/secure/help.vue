<script lang="ts" setup>
import { ref } from "vue";
import { GptClient, GptEntity } from "../autogeneratedServices";

const showChat = ref(false);
const myChats = ref<{ text: string; isFromMe: boolean; }[]>([{ isFromMe: false, text: "Hei, jeg er chat gpt. Jeg kan svare på spørsmål om beredskap og førstehjelp. Det er viktig å huske at jeg bare er en robot og du bør alltid snakke med erfarne fagfolk først. Det er mer jeg ikke vet enn du tror." }]);
const message = ref("");

async function send() {
	const m = message.value + "";
	if (message.value === "") {
		return;
	}
	myChats.value.push({ isFromMe: true, text: message.value });
	message.value = "";

	try {
		const api = new GptClient("");
		const response = await api.generateResult(new GptEntity({ text: m }));
		myChats.value.push({ isFromMe: false, text: response });
	}
	catch (err) {
		myChats.value.push({ isFromMe: false, text: "Beklager, jeg fikk ikke tilgang til serveren for øyeblikket. Prøv igjen senere." });
	}
}
</script>